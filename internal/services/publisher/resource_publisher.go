func (r *Resource) ImportState(ctx context.Context, req resource.ImportStateRequest, resp *resource.ImportStateResponse) {
	ctx, exitContext := helpers.EnterRequestContext(ctx, r.TypeInfo, req)
	defer exitContext()

	parts := strings.SplitN(req.ID, "_", 2)
	if len(parts) != 2 || !isGuid(parts[0]) || !isGuid(parts[1]) {
		resp.Diagnostics.AddError(
			"Invalid import ID",
			fmt.Sprintf("Expected import ID in format 'environment_id_publisher_id', got '%s'", req.ID),
		)
		return
	}

	resp.Diagnostics.Append(resp.State.SetAttribute(ctx, path.Root("environment_id"), parts[0])...)
	resp.Diagnostics.Append(resp.State.SetAttribute(ctx, path.Root("id"), parts[1])...)
}

func validateAddressSlots(addresses []PublisherAddressModel) (diags diag.Diagnostics) {
	seen := map[int64]struct{}{}
	for idx, address := range addresses {
		if address.Slot.IsNull() || address.Slot.IsUnknown() {
			diags.AddAttributeError(
				path.Root("address").AtListIndex(idx).AtName("slot"),
				"Missing address slot",
				"Each address entry must declare a slot value of 1 or 2.",
			)
			continue
		}

		slot := address.Slot.ValueInt64()
		if _, exists := seen[slot]; exists {
			diags.AddAttributeError(
				path.Root("address").AtListIndex(idx).AtName("slot"),
				"Duplicate address slot",
				fmt.Sprintf("Only one address entry may target slot %d.", slot),
			)
			continue
		}
		seen[slot] = struct{}{}
	}
	return diags
}

