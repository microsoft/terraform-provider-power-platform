func effectiveCustomizationOptionValuePrefix(model *ResourceModel) int64 {
	if !model.CustomizationOptionValuePrefix.IsNull() && !model.CustomizationOptionValuePrefix.IsUnknown() {
		return model.CustomizationOptionValuePrefix.ValueInt64()
	}

	return deriveCustomizationOptionValuePrefix(model.CustomizationPrefix.ValueString(), model.Id.ValueString())
}

func deriveCustomizationOptionValuePrefix(prefix, publisherId string) int64 {
	if publisherId == "d21aab71-79e7-11dd-8874-00188b01e34f" {
		return 10000
	}

	normalizedPrefix := strings.ToUpper(prefix)
	return customizationOptionValuePrefixFromHash(hashCustomizationPrefix(normalizedPrefix))
}

func hashCustomizationPrefix(prefix string) int32 {
	var hash int32
	for _, ch := range prefix {
		hash = (hash << 5) - hash + ch
	}
	return hash
}

func customizationOptionValuePrefixFromHash(hash int32) int64 {
	value := int64(hash)
	if value < 0 {
		value = -value
	}
	return (value % 90000) + 10000
}

func isPublisherAlreadyExistsError(err error) bool {
	var unexpectedStatusErr customerrors.UnexpectedHttpStatusCodeError
	if !errors.As(err, &unexpectedStatusErr) {
		return false
	}

	if unexpectedStatusErr.StatusCode != http.StatusPreconditionFailed {
		return false
	}

	body := string(unexpectedStatusErr.Body)
	return strings.Contains(body, `"code":"0x80040237"`) ||
		strings.Contains(body, "matching key values already exists")
}

