		body[prefix+"line3"] = nullableStringPointer(address.Line3)
		body[prefix+"longitude"] = nullableFloat64Pointer(address.Longitude)
		body[prefix+"name"] = nullableStringPointer(address.Name)
		body[prefix+"postalcode"] = nullableStringPointer(address.PostalCode)
		body[prefix+"postofficebox"] = nullableStringPointer(address.PostOfficeBox)
		body[prefix+"shippingmethodcode"] = nullableInt64Pointer(address.ShippingMethodCode)
		body[prefix+"stateorprovince"] = nullableStringPointer(address.StateOrProvince)
