func (r *Resource) Create(ctx context.Context, req resource.CreateRequest, resp *resource.CreateResponse) {
	ctx, exitContext := helpers.EnterRequestContext(ctx, r.TypeInfo, req)
	defer exitContext()

	var plan ResourceModel
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}

	resp.Diagnostics.Append(validateAddressSlots(plan.Address)...)
	if resp.Diagnostics.HasError() {
		return
	}

	publisher, err := r.PublisherClient.CreatePublisher(ctx, plan.EnvironmentId.ValueString(), &plan)
	if err != nil {
		if isPublisherAlreadyExistsError(err) {
			existing, lookupErr := r.PublisherClient.GetPublisherByUniqueName(ctx, plan.EnvironmentId.ValueString(), plan.UniqueName.ValueString())
			if lookupErr == nil && existing != nil {
				resp.Diagnostics.AddError(
					"Publisher already exists",
					fmt.Sprintf(
						"A Dataverse publisher with unique name `%s` already exists in environment `%s`. Import it using `terraform import <resource-address> %s` or choose a different `uniquename`.",
						plan.UniqueName.ValueString(),
						plan.EnvironmentId.ValueString(),
						buildPublisherImportId(plan.EnvironmentId.ValueString(), existing.Id),
					),
				)
				return
			}

			resp.Diagnostics.AddError(
				"Publisher already exists",
				fmt.Sprintf(
					"A Dataverse publisher with unique name `%s` already exists in environment `%s`. Import the existing publisher into state or choose a different `uniquename`.",
					plan.UniqueName.ValueString(),
					plan.EnvironmentId.ValueString(),
				),
			)
			return
		}

		resp.Diagnostics.AddError(fmt.Sprintf("Client error when creating %s", r.FullTypeName()), err.Error())
		return
	}

	setResourceModelFromDto(&plan, plan.EnvironmentId.ValueString(), publisher)
	resp.Diagnostics.Append(resp.State.Set(ctx, &plan)...)
}

func (r *Resource) Read(ctx context.Context, req resource.ReadRequest, resp *resource.ReadResponse) {
	ctx, exitContext := helpers.EnterRequestContext(ctx, r.TypeInfo, req)
	defer exitContext()

	var state ResourceModel
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	publisher, err := r.PublisherClient.GetPublisherById(ctx, state.EnvironmentId.ValueString(), state.Id.ValueString())
	if err != nil {
		if errors.Is(err, customerrors.ErrObjectNotFound) {
			resp.State.RemoveResource(ctx)
			return
		}
		resp.Diagnostics.AddError(fmt.Sprintf("Client error when reading %s", r.FullTypeName()), err.Error())
		return
	}

	setResourceModelFromDto(&state, state.EnvironmentId.ValueString(), publisher)
	resp.Diagnostics.Append(resp.State.Set(ctx, &state)...)
}

